import java.util.*;

class NestedFor{

        public static void main(String[] args){

                Scanner sc = new Scanner(System.in);
                System.out.print("enter number of row:");
                int rows = sc.nextInt();

                for(int i = 0; i<rows; i++){

                        for(int j = 0; j<rows; j++){

                              System.out.print("c2w ");
                        }
                        System.out.println();
                }
        }
}
